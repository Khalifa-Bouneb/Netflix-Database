-- Table ABONNEMENT
CREATE TABLE ABONNEMENT (
    id_abonnement NUMBER(10) PRIMARY KEY,
    date_début DATE,
    date_fin DATE,
    prix NUMBER(8,2)
);
-- Table ABONNEMENT_PREMIUM
CREATE TABLE ABONNEMENT_PREMIUM (
    id_abonnement NUMBER(10) PRIMARY KEY,
    nb_max_ecrans NUMBER(2),
    qualité_video VARCHAR2(20),
    CONSTRAINT FK_ABONNEMENT_PREMIUM FOREIGN KEY (id_abonnement) REFERENCES ABONNEMENT(id_abonnement)
);
-- Table ABONNEMENT_ETUDIANT
CREATE TABLE ABONNEMENT_ETUDIANT (
    id_abonnement NUMBER(10) PRIMARY KEY,
    justificatif_études VARCHAR2(255),
    réduction_appliquée NUMBER(6,2),
    CONSTRAINT FK_ABONNEMENT_ETUDIANT FOREIGN KEY (id_abonnement) REFERENCES ABONNEMENT(id_abonnement)
);
-- Table ABONNEMENT_STANDARD
CREATE TABLE ABONNEMENT_STANDARD (
    id_abonnement NUMBER(10) PRIMARY KEY,
    nb_ecrans_max NUMBER(2),
    CONSTRAINT FK_ABONNEMENT_STANDARD FOREIGN KEY (id_abonnement) REFERENCES ABONNEMENT(id_abonnement)
);
-- Table UTILISATEUR
CREATE TABLE UTILISATEUR (
    id_utilisateur NUMBER(10) PRIMARY KEY,
    nom_user VARCHAR2(50),
    prenom_user VARCHAR2(50),
    email_user VARCHAR2(100),
    mot_de_passe VARCHAR2(24),
    date_de_naissance DATE,
    date_inscri DATE,
    id_abonnement NUMBER(10),
    CONSTRAINT FK_UTILISATEUR_ABONNEMENT FOREIGN KEY (id_abonnement) REFERENCES ABONNEMENT(id_abonnement)
);
-- Table PAIEMENT
CREATE TABLE PAIEMENT (
    id_paiement NUMBER(10) PRIMARY KEY,
    date_paiement DATE,
    montant NUMBER(10),
    moyen_paiement VARCHAR2(20),
    id_utilisateur NUMBER(10),
    CONSTRAINT FK_PAIEMENT_UTILISATEUR FOREIGN KEY (id_utilisateur) REFERENCES UTILISATEUR(id_utilisateur)
);
-- Table PROFIL
CREATE TABLE PROFIL (
    id_profil NUMBER(10) PRIMARY KEY,
    nom_profil VARCHAR2(50),
    id_utilisateur NUMBER(10),
    CONSTRAINT FK_PROFIL_UTILISATEUR FOREIGN KEY (id_utilisateur) REFERENCES UTILISATEUR(id_utilisateur)
);
-- Table PROFIL_ADULTE
CREATE TABLE PROFIL_ADULTE (
    id_profil NUMBER(10) PRIMARY KEY,
    id_profil1 NUMBER(10),
    id_parent NUMBER(10),
    CONSTRAINT FK_PROFIL_ADULTE FOREIGN KEY (id_profil) REFERENCES PROFIL(id_profil)
);
-- Table PROFIL_ENFANT
CREATE TABLE PROFIL_ENFANT (
    id_profil NUMBER(10) PRIMARY KEY,
    id_profil1 NUMBER(10),
    nom_tuteur VARCHAR2(50),
    parent_id NUMBER(10),
    date_naissance_enfant DATE,
    CONSTRAINT FK_PROFIL_ENFANT FOREIGN KEY (id_profil) REFERENCES PROFIL(id_profil),
    CONSTRAINT FK_PROFIL_ENFANT_PARENT FOREIGN KEY (parent_id) REFERENCES PROFIL_ADULTE(id_profil)
);
-- Table CONTENU
CREATE TABLE CONTENU (
    id_contenu NUMBER(10) PRIMARY KEY,
    titre VARCHAR2(100),
    description VARCHAR2(1000),
    date_sortie DATE,
    langue_original VARCHAR2(30)
);
-- Table SERIE
CREATE TABLE SERIE (
    id_contenu NUMBER(10) PRIMARY KEY,
    nbre_episode NUMBER(5),
    nbre_saison NUMBER(3),
    CONSTRAINT FK_SERIE_CONTENU FOREIGN KEY (id_contenu) REFERENCES CONTENU(id_contenu)
);
-- Table FILM
CREATE TABLE FILM (
    id_contenu NUMBER(10) PRIMARY KEY,
    durée NUMBER(5),
    CONSTRAINT FK_FILM_CONTENU FOREIGN KEY (id_contenu) REFERENCES CONTENU(id_contenu)
);
-- Table DONNE_AVIS
CREATE TABLE DONNE_AVIS (
    id_evaluation NUMBER(10) PRIMARY KEY,
    nbr_etoile NUMBER(1),
    commentaire VARCHAR2(500),
    date_eva DATE,
    id_profil NUMBER(10),
    id_contenu NUMBER(10),
    CONSTRAINT FK_DONNE_AVIS_PROFIL FOREIGN KEY (id_profil) REFERENCES PROFIL(id_profil),
    CONSTRAINT FK_DONNE_AVIS_CONTENU FOREIGN KEY (id_contenu) REFERENCES CONTENU(id_contenu)
);
-- Table HISTORIQUE_VISIONNAGE
CREATE TABLE HISTORIQUE_VISIONNAGE (
    id_historique_visionnage NUMBER(10) PRIMARY KEY,
    date_visionnage DATE,
    temps_ecoulé NUMBER(8),
    id_profil NUMBER(10),
    id_contenu NUMBER(10),
    CONSTRAINT FK_HISTORIQUE_VISIONNAGE_PROFIL FOREIGN KEY (id_profil) REFERENCES PROFIL(id_profil),
    CONSTRAINT FK_HISTORIQUE_VISIONNAGE_CONTENU FOREIGN KEY (id_contenu) REFERENCES CONTENU(id_contenu)
